using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MethodsAssignment
{
    public class byFive
    {
        public int timesFive(int user)
        {
            return user * 5;
        }
        public int plusFive(int user)
        {
            return user + 5;
        }
        public int minusFive(int user)
        {
            return user - 5;
        }
    }
}
